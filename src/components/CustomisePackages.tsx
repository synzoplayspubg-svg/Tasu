// Robust helper to resolve string or raw JSON-serialized icons to absolute Lucide React components
const resolvePlanIcon = (iconInput: any, defaultIcon: React.ComponentType<any>): React.ComponentType<any> => {
  if (!iconInput) {
    return defaultIcon;
  }
  
  if (typeof iconInput === "function" || (typeof iconInput === "object" && iconInput.$$typeof)) {
    return iconInput || defaultIcon;
  }
  
  if (typeof iconInput === "string") {
    const iconStr = iconInput.trim();
    switch (iconStr) {
      case "Laptop": return Laptop || defaultIcon;
      case "Settings": return Settings || defaultIcon;
      case "Globe": return Globe || defaultIcon;
      case "Shield": return Shield || defaultIcon;
      case "ArrowRight": return ArrowRight || defaultIcon;
      case "Zap": return Zap || defaultIcon;
      case "Sparkles": return Sparkles || defaultIcon;
      case "PenTool": return PenTool || defaultIcon;
      case "Coins": return Coins || defaultIcon;
      case "ChevronLeft": return ChevronLeft || defaultIcon;
      case "ChevronRight": return ChevronRight || defaultIcon;
      case "ShoppingCart": return ShoppingCart || defaultIcon;
      case "Store": return Store || defaultIcon;
      case "Utensils": return Utensils || defaultIcon;
      case "Briefcase": return Briefcase || defaultIcon;
      case "User": return User || defaultIcon;
      case "GraduationCap": return GraduationCap || defaultIcon;
      case "BookOpen": return BookOpen || defaultIcon;
      case "FileText": return FileText || defaultIcon;
      case "Newspaper": return Newspaper || defaultIcon;
      case "Gamepad": return Gamepad || defaultIcon;
      case "X": return X || defaultIcon;
      case "Clock": return Clock || defaultIcon;
      default: return defaultIcon;
    }
  }
  
  return defaultIcon;
};

